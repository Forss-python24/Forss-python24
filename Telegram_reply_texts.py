def add_text(text, data):


    while True:
        try :
            if data:
                message = data.capitalize()

                length = len(text.splitlines())

                text = text + str((length-1)) + "-" + message
                return text
        except:
            pass

def File_writer(text, data):

    with open("Text_replies.txt", "w") as file:
        try:
            file.write(add_text(text, data)) 
            file.close() 
        except:
            pass


